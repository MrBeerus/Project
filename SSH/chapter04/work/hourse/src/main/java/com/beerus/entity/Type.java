package com.beerus.entity;

/**
 * 类型表
 */
public class Type {

  private Integer id;
  private String name;

  public Type() {
  }

  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

}
